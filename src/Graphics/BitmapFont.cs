    // Draw/write/whatever a single character at specified position
    // Now includes background color to properly clear previous characters
    public static void DrawChar(IntPtr renderer, char c, int x, int y, 
                                byte r, byte g, byte b,
                                byte bgR = 0, byte bgG = 0, byte bgB = 0)
    {
        int index = c;
        
        // valid range
        if (index < 0 || index >= 256)
            index = 32; // space for invalid chars
        
        byte[] charData = fontData[index];
        
        // Draw pixels of character (including background)
        for (int row = 0; row < CHAR_HEIGHT; row++)
        {
            byte rowData = charData[row];
            for (int col = 0; col < CHAR_WIDTH; col++)
            {
                // Check if pixel should be drawn (bit is set)
                // Read bits from LEFT to RIGHT (MSB first) - standard font format
                // We dont want to read error after each pixel, we ignore the check and VS Studio complaints with: _ = 
                if ((rowData & (0x80 >> col)) != 0)
                {
                    // Foreground color (character pixel)
                    _ = SDL.SDL_SetRenderDrawColor(renderer, r, g, b, 255);
                }
                else
                {
                    // Background color (clears previous content)
                    _ = SDL.SDL_SetRenderDrawColor(renderer, bgR, bgG, bgB, 255);
                }
                _ = SDL.SDL_RenderDrawPoint(renderer, x + col, y + row);
            }
        }
    }
    
    // Draw a string at specified position
    public static void DrawString(IntPtr renderer, string text, int x, int y, byte r, byte g, byte b)
    {
        int currentX = x;
        
        foreach (char c in text)
        {
            if (c == '\n')
            {
                // Newline handling would go here
                continue;
            }
            
            DrawChar(renderer, c, currentX, y, r, g, b);
            currentX += CHAR_WIDTH;
        }
    }
    
